/*
  Filename:     fixPrinter.c
  Purpose:      Part of the compiler front-end for the HAL/S language.
                It is a fix for the Printer.c files auto-generated by
                BNF Converter, and needs to be #include'd first thing
                in
                       void bufAppendC(const char c)
                       {
                         ...
                       }
                in Printer.c.  (Done automatically by makefile.)
  Compiler:     GNU gcc.
  Reference:    http://www.ibibio.org/apollo
  Mods:         2022-12-12 RSB  Created
*/

#ifdef IGNORE_THIS_COMPLETELY
  static int indent = 0;
  static char indentString[1000]; // Or some other big number.
  if (c == '(')
    {
      //sprintf(indentString, "\n%*s%d ", indent, "", indent);
      sprintf(indentString, "\n%d ", indent);
      indent++;
      bufAppendS(indentString);
      return;
    }
  if (c == ')')
    {
      indent--;
      return;
    }
#endif
  if (c == '\"')
    {
      bufAppendC('^');
      return;
    }
